/**
 * A combination of two elements into a resulting element.
 */
class Combination {
  /**
   * Creates a combination.
   * @param element1 {String} The id of the first element in the combination
   * @param element2 {String} The id of the second element in the combination
   * @param result {String} The id of the resulting element
   */
  constructor(element1, element2, result) {
    this.element1 = element1;
    this.element2 = element2;
    this.result = result;
  }
}